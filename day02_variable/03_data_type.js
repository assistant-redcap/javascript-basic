// string
const name1 = "jony";

// number : 정수, 실수, 음수 다 됨
const num1 = 1;
const num2 = 0.123;
const num3 = -12;

// boolean : true / false
const flag = true;

console.log(typeof name1);  // string
console.log(typeof num1);   // number
console.log(typeof num2);   // number
console.log(typeof num3);   // number
console.log(typeof flag);   // boolean

// boolean 의 반대는 느낌표
console.log(!flag)  // false